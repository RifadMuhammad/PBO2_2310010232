    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
            if (txtIdToko.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"ID toko belum diisi");
                txtIdToko.requestFocus();
            } else if (txtNamaToko.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Nama toko belum diisi");
                txtNamaToko.requestFocus();
            } // Tambahkan validasi lain jika perlu
            else {
                // Ambil data
                String id_toko = txtIdToko.getText();
                String nama_toko = txtNamaToko.getText();
                String alamat_toko = txtAlamatToko.getText();
                String no_hp_toko = txtNoHpToko.getText();
                String rating = txtRating.getText();
                String produk = txtProduk.getText();
                String tgl_daftar_user = txtTglDaftar.getText();
                
                // Panggil method simpan
                objekKu.simpanToko01(id_toko, nama_toko, alamat_toko, no_hp_toko, rating, produk, tgl_daftar_user);
                
                // Refresh tabel
                objekKu.tampilDataToko(tableToko, "select * from toko");
                
                // Cek jika data duplikat
                if (objekKu.validasi == true){
                    // Tampilkan data yang sudah ada jika ID terdeteksi duplikat
                    txtNamaToko.setText(objekKu.VAR_NAMA_TOKO);
                    txtAlamatToko.setText(objekKu.VAR_ALAMAT_TOKO);
                    txtNoHpToko.setText(objekKu.VAR_NO_HP_TOKO);
                    txtRating.setText(objekKu.VAR_RATING);
                    txtProduk.setText(objekKu.VAR_PRODUK);
                    // txtTglDaftar tidak ada di variabel crud, jadi biarkan
                } else {
                    clearForm(); // Bersihkan form jika berhasil disimpan
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            if (txtIdToko.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"ID toko belum diisi");
                txtIdToko.requestFocus();
            } else {
                // Ambil data
                String id_toko = txtIdToko.getText();
                String nama_toko = txtNamaToko.getText();
                String alamat_toko = txtAlamatToko.getText();
                String no_hp_toko = txtNoHpToko.getText();
                String rating = txtRating.getText();
                String produk = txtProduk.getText();
                String tgl_daftar_user = txtTglDaftar.getText();
                
                // Panggil method ubah
                objekKu.ubahToko01(id_toko, nama_toko, alamat_toko, no_hp_toko, rating, produk, tgl_daftar_user);
                
                // Refresh tabel dan bersihkan form
                objekKu.tampilDataToko(tableToko, "select * from toko");
                clearForm();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
             if (txtIdToko.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"ID toko belum diisi");
                txtIdToko.requestFocus();
            } else {
                objekKu.hapusToko01(txtIdToko.getText());
                clearForm(); // Bersihkan form
                // Refresh tabel
                objekKu.tampilDataToko(tableToko, "select * from toko");
             }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed


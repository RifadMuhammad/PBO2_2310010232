    private void txtCariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyPressed
        // Mencari berdasarkan nama toko atau alamat
        String cari = "select * from toko where nama_toko like '%"+txtCari.getText()+"%'"
                + "OR alamat_toko like '%"+txtCari.getText()+"%' ";
        objekKu.tampilDataToko(tableToko, cari);
    }//GEN-LAST:event_txtCariKeyPressed

    private void tableTokoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTokoMouseClicked
        // Mengisi form saat data di tabel diklik
        try {
            int baris = tableToko.getSelectedRow();
            txtIdToko.setText(tableToko.getValueAt(baris, 0).toString());
            txtNamaToko.setText(tableToko.getValueAt(baris, 1).toString());
            txtAlamatToko.setText(tableToko.getValueAt(baris, 2).toString());
            txtNoHpToko.setText(tableToko.getValueAt(baris, 3).toString());
            txtRating.setText(tableToko.getValueAt(baris, 4).toString());
            txtProduk.setText(tableToko.getValueAt(baris, 5).toString());
            txtTglDaftar.setText(tableToko.getValueAt(baris, 6).toString());
        } catch (Exception e) {
            //
        }
    }//GEN-LAST:event_tableTokoMouseClicked

